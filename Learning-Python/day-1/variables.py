# variable is a container for storing data values. It is a way to give a name to a value so the value can be used later in the program. In Python, you can create a variable by assigning a value to it using the equals sign (=). There are different types of variables in Python, including: 
# - Integer (int): Represents whole numbers, e.g., 5, -3, 0
# - Float (float): Represents decimal numbers, e.g., 3.14, -2.5, 0.0
# - String (str): Represents a sequence of characters, e.g., "hello", 'Python'
# - Boolean (bool): Represents either True or False
# - List (list): Represents an ordered collection of items, e.g., [1, 2, 3]
# - Tuple (tuple): Represents an ordered, immutable collection of items, e.g., (1, 2, 3)
# - Dictionary (dict): Represents a collection of key-value pairs, e.g., {'name': 'Alice', 'age': 30}
